class SwitchExample {
    public static void main(String[] args) {
        
        switch(5){
            case 1:
            System.out.println(1);
           
            case 2:
            System.out.println(2);
           
            case 3:
            System.out.println(3);
            
            default:
            System.out.println("default");

            case 4:
            System.out.println(4);

            case 5:
            System.out.println(5);
        }

        System.out.println("---------------");

        switch(8){
            case 1:
            System.out.println(1);
            break;
            case 2:
            System.out.println(2);
            break;
            case 3:
            System.out.println(3);
            break;
            default:
            System.out.println("default");
            break;

            case 4:
            System.out.println(4);
            break;

            case 5:
            System.out.println(5);
            break;
        }


    }
}
