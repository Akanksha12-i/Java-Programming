class Variables{

public static void main(String[] args){
byte b;
b=10;  //range-> -128 t0 127
System.out.println(b);

short s=2222;   //range- -32768 to +32768
System.out.println(s);

int i=23568;     //range- -2147483648 to +2147483648
System.out.println(i);

long l=1234567894567889l;   //range- -9223372036854775807 to +9223372036854775807
System.out.println(l);

float f=34.789f;
System.out.println(f);

double d=34.78978;  //either can give d/D at last or can leave
System.out.println(d);

char ch='A';
System.out.println(ch);

boolean bool=true;
System.out.println(bool);


}

}